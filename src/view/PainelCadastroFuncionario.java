    private void buttonupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonupdateActionPerformed
        if(roldados.getSelectedRow() != -1){
            FuncionarioDAO _fun = new FuncionarioDAO();
            Funcionario fun = new Funcionario();

            fun.setNome(inputnome.getText());
            fun.setDt_nasc(inputdata.getText());
            fun.setSexo(inputsexo.getText());
            fun.setId((int)roldados.getValueAt(roldados.getSelectedRow(), 0));

            
            _fun.update(fun);
            
            inputnome.setText("");
            inputdata.setText("");
            inputsexo.setText("");
            
            JOptionPane.showMessageDialog(null, "Atualizado com Sucesso!");

            Atualizatable();
        }
    }//GEN-LAST:event_buttonupdateActionPerformed

