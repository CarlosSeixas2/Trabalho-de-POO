    private void buttoncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttoncadastrarActionPerformed
        FuncionarioDAO _fun = new FuncionarioDAO();
        Funcionario fun = new Funcionario();
        
        fun.setNome(inputnome.getText());
        fun.setDt_nasc(inputdata.getText());
        fun.setSexo(inputsexo.getText());
        
        if(inputnome.getText().isEmpty() || inputdata.getText().isEmpty() || inputsexo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Confira se algo não está faltando");
        }else{
        
            DefaultTableModel tabela = (DefaultTableModel) roldados.getModel();

            Object[] dados = {
                fun.getId(),
                fun.getNome(),
                fun.getDt_nasc(),
                fun.getSexo()
            };
            tabela.addRow(dados);

            try {
                _fun.insert(fun);
                Atualizatable();
                inputnome.setText("");
                inputdata.setText("");
                inputsexo.setText("");

                JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso!");
            } catch (SQLException ex) {
                Logger.getLogger(PainelCadastroCarro.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Eroo ao Cadastrar!");

            }
        }
    }//GEN-LAST:event_buttoncadastrarActionPerformed

    private void roldadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_roldadosMouseClicked
        if(roldados.getSelectedRow() != -1){
            inputnome.setText(roldados.getValueAt(roldados.getSelectedRow(), 1).toString());
            inputdata.setText(roldados.getValueAt(roldados.getSelectedRow(), 2).toString());
            inputsexo.setText(roldados.getValueAt(roldados.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_roldadosMouseClicked

